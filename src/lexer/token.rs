static KEYWORD_LOOKUP: OnceLock<HashMap<&'static str, TokenKind>> = OnceLock::new();
static SYMBOL_LOOKUP: OnceLock<HashMap<&'static str, TokenKind>> = OnceLock::new();

impl TokenKind {
    pub fn from_keyword(value: &str) -> Option<Self> {
        KEYWORD_LOOKUP
            .get_or_init(|| KEYWORD_TOKENS.iter().copied().collect())
            .get(value)
            .copied()
    }

    pub fn from_symbol(value: &str) -> Option<Self> {
        SYMBOL_LOOKUP
            .get_or_init(|| SYMBOL_TOKENS.iter().copied().collect())
            .get(value)
            .copied()
    }
}